package dailycode;

public class Incremenr_decrement {

	public static void main(String[] args) {

		int a = 4, b = 2, c = 6;
		System.out.println(a);
		System.out.println(a++);
		a = a + 20;
		System.out.println(a);
		b++;
		b = b + 20;
		System.out.println(b);
		System.out.println(a + b);
		--b;
		System.out.println(b + 10);
		System.out.println(++b);
		System.out.println(a + b + c);
	}

}
