package dailycode;

public class vowel_consonant {

	public static void main(String[] args) {
		char ch = 'E';
		if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' || ch == 'A' 
				|| ch == 'E' || ch == 'I'|| ch == 'O' || ch == 'U') {
			System.out.println(ch + " is vowel");
		} else
			System.out.println(ch + " is not vowel");
	}

}
