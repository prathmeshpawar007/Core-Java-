package dailycode;

public class addition {
	public static void main(String[] args) {
		int a = 40;
		int b = 2;

		int c = a + b;
		System.out.println(c);

		c = a - b;
		System.out.println(c);

		c = a / b;
		System.out.println(c);

		c = a * b;
		System.out.println(c);

		c = a % b;
		System.out.println(c);

	}
}