package Collection;

import java.util.ArrayList;
import java.util.Scanner;

public class ArrayList_simple_arraylist {

	public static void main(String[] args) {
		
		
		ArrayList AL = new ArrayList();
		
		
		AL.add(10);
		AL.add(20);
		AL.add(10.5);
		AL.add("PP");
		AL.add('A');
		
		
		System.out.println(AL);
		
		

	}

}
